package com.example.meet6;

public class person {
int img;
String name;
String age;


    public person() {
    }

    public person(int img, String name, String age) {
        this.img = img;
        this.name = name;
        this.age = age;

    }



    public int getImg() {
        return img;
    }

    public String getName() {
        return name;
    }

    public String getAge() {
        return age;
    }
}
